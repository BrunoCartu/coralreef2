package coralreef2.InputData;

public class Sand_cover {
	

	private double year ;
	private double sandCover ;
	
	public Sand_cover(final double year,final double sandCover){
		this.year = year ;
		this.sandCover = sandCover ;
	}
	
	public double getYear(){
		return this.year ;
	}
	public double getSand_Cover (){
		return this.sandCover ;
	}
	
}


